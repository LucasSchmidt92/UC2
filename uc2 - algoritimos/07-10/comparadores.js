let numero1 = 90
let numero2 = 45

//comparar se um é igual ao outro
console.log (numero1 === numero2) // False







//comparar se um é diferente ao outro
console.log (numero1 !== numero2) // true



//comparar se um é igual ao outro
console.log (numero1 === numero2) // False



//comparar se um NUMERO É maior que outro
console.log (numero1 > numero2) // true



//comparar se um é igual ou maior ao outro
console.log (numero1 >= numero2) // true



//comparar se um é menor ao outro
console.log (numero1 < numero2) // False


//comparar se um é igual ou menor
console.log (numero1 <= numero2) // False

